import java.util.*;

public class operaciones {

    public operaciones() {
    }

    static Scanner sc = new Scanner(System.in);

    public static double Sumar(double sum1, double sum2) {
        double sum3 = sum1 + sum2;
        return sum3;
    }


    public static double Restar(double sum1, double sum2) {
        double sum3 = sum1 - sum2;
        return sum3;
    }

    public static double Multiplicar(double sum1, double sum2) {

        double sum3 = sum1 * sum2;
        return sum3;
    }

    public static double Dividir(double sum1, double sum2) {
        double sum3 = sum1 / sum2;
        return sum3;
    }


}
